    public void LeerEXCEL(File archivo) {
        //XSSFWorkbook libro = new XSSFWorkbook();
        //String nombreArchivo = "C:/Users/Lenovo/Desktop/java.xlsx";
        String nombreArchivo = archivo.getAbsolutePath();
        System.out.println(archivo.getName());
        File excel = new File(nombreArchivo);
        List cellData = new ArrayList();
        try {
            //JTextArea txtConsola = new JTextArea();
            txtarea.append("Leyendo archivo " + nombreArchivo + "\n");

            FileInputStream fis = new FileInputStream(excel);
            XSSFWorkbook workBook = new XSSFWorkbook(fis);

            XSSFSheet hoja = workBook.getSheetAt(0);
            Iterator rowIterator = hoja.rowIterator();
            while (rowIterator.hasNext()) {
                //txtarea.append("*");
                XSSFRow row = (XSSFRow) rowIterator.next();
                Iterator iterator = row.cellIterator();
                List cellTemp = new ArrayList();
                while (iterator.hasNext()) {
                    XSSFCell cell = (XSSFCell) iterator.next();
                    cellTemp.add(cell);
                }
                txtarea.append("_");
                cellData.add(cellTemp);
            }
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
        }
        txtarea.append("100% \n");
        obtenerDatos(cellData,archivo);
    }

    public void obtenerDatos(List cellList,File archivo) {
        String rutaArchivo = archivo.getAbsolutePath();
        String hoja = "Majito";
        XSSFWorkbook libro = new XSSFWorkbook();
        XSSFSheet hoja1 = libro.createSheet(hoja);
        txtarea.append("Creando archivo \n");
        // Generar los datos para el documento        
        //txtarea.setForeground(Color.red);
        //String valorCelda="";
        double aleatorio ;
        for (int i = 0; i < cellList.size(); i++) {
            txtarea.append("*");
            List listado = (List) cellList.get(i);
            XSSFRow row = hoja1.createRow(i);
            for (int j = 0; j < listado.size(); j++) {                
                XSSFCell cell = row.createCell(j);
                XSSFCell celdas = (XSSFCell) listado.get(j);
                String valor = celdas.toString();
                //System.out.print(cellList.get(14)+","+listado.get(0)+"primera fila");
                if ((valor == "")&& (i > 4) ) {
                	aleatorio = Math.random()*1;
                        if(aleatorio > 0.5){
                            //valorCelda =String.valueOf(1);
                            cell.setCellValue(1);
                        }else{
                            //valorCelda =String.valueOf(0);
                            cell.setCellValue(0);
                        }                	
                    //cell.setCellValue(aleatorio);
                }else{
                 cell.setCellValue(valor);
                }
            }
            //System.out.println();
        }
        txtarea.append("100% \n");
        // Crear el archivo
        try (OutputStream fileOut = new FileOutputStream(rutaArchivo)) {
            //System.out.println("SE CREO EL EXCEL");
            txtarea.append("Archivo Generado con Ã©xito \n");
            txtarea.append("En la ruta: \n" + rutaArchivo );
            libro.write(fileOut);
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
